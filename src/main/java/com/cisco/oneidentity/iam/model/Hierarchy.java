package com.cisco.oneidentity.iam.model;

public class Hierarchy {
	
	private String guName;	 
	private String parentName;
	private String duName;	 
	private String guDunsNumber;
	private String parentDunsNumber;	 
	private String duDunsNumber;
	
	public String getGuName() {
		return guName;
	}
	public void setGuName(String guName) {
		this.guName = guName;
	}
	public String getParentName() {
		return parentName;
	}
	public void setParentName(String parentName) {
		this.parentName = parentName;
	}
	public String getDuName() {
		return duName;
	}
	public void setDuName(String duName) {
		this.duName = duName;
	}
	public String getGuDunsNumber() {
		return guDunsNumber;
	}
	public void setGuDunsNumber(String guDunsNumber) {
		this.guDunsNumber = guDunsNumber;
	}
	public String getParentDunsNumber() {
		return parentDunsNumber;
	}
	public void setParentDunsNumber(String parentDunsNumber) {
		this.parentDunsNumber = parentDunsNumber;
	}
	public String getDuDunsNumber() {
		return duDunsNumber;
	}
	public void setDuDunsNumber(String duDunsNumber) {
		this.duDunsNumber = duDunsNumber;
	}

	
}
